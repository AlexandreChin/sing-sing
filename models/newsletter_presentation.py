"""Models for the newsletter presentation layer.

Newsletter-specific prose generated by newsletter_adapt_agent — the long-form
prose render of the optimized carousel deck, expanded into detailed paragraphs
(not slide fragments), neutral (no grade/verdict). The arc mirrors the
carousel's four acts:

  Header/hook → Pourquoi cet article → Avant de vous lancer →
  Au fil de la lecture → Après la lecture (L'architecture de l'argument,
  À emporter, À vous de juger, Prolonger la réflexion) → signoff
"""
from __future__ import annotations
from pydantic import BaseModel
from typing import Literal


class DecryptageItem(BaseModel):
    """One reading moment in the chronological "Au fil de la lecture" pass, in
    article order. Neutral: no fait/faille grade badge is rendered — `kind` and
    `presentation` are kept for schema compatibility but unused by the templates."""
    kind: Literal["fait", "faille"]   # unused by rendering — kept for compatibility
    quote: str          # the article sentence examined (verbatim, « … »)
    presentation: str   # unused by rendering — kept for compatibility
    reading: str        # our neutral critical reading = the ANSWER (rendered "Réponse —"), 2–4 sentences
    prompt: str | None = None   # ≤12 words — the lens-tagged instruction ("what to spot"), rendered before the answer
    lens_ref: str | None = None   # canonical lens id (agent/lenses.py) this beat exercises — supplies the icon+name tag


class Resource(BaseModel):
    title: str
    source: str
    why: str            # 1–2 sentences — what it adds / why explore it
    type: str | None = None   # kind of resource — "étude", "documentaire", "livre", "rapport", "podcast", "article"…
    url: str | None = None    # ONLY a stable canonical URL; blank otherwise (filled during manual fine-tuning)


class Reflexe(BaseModel):
    """A lens-anchored critical-reading reflex — the newsletter's transferable payoff
    (rendered under "Les réflexes critiques"). Anchored to a canonical lens
    (agent/lenses.py), which supplies its icon + name so it matches the beats that
    exercised it. `rule` is this article's concrete application; `reusable_on` names
    other domains it generalizes to."""
    lens_ref: str      # canonical lens id (agent/lenses.py) — supplies icon + name
    rule: str          # the transferable one-liner (this article's application)
    reusable_on: str | None = None   # domains it generalizes to — "santé, économie, sondages"


class Architecture(BaseModel):
    """L'architecture de l'argument — the thesis's unstated supports + the question
    that tests them (mirrors the carousel slide 8: core_recap = présupposés,
    engagement question = "La question")."""
    keystone: str              # "La question" — the reader-question that tests the présupposés
    presupposes: list[str]     # 2–4 — the implicit premises the thesis holds for granted


class AEmporter(BaseModel):
    """À emporter — what to keep: the key takeaways + the critical-reading reflexes recap."""
    key_takeaways: list[str]       # 4–6 — À retenir (the article's load-bearing facts)
    reflexes_critiques: list[Reflexe]  # 3–5 — Les réflexes critiques (named, collectible reflexes)


class AVousDeJuger(BaseModel):
    """À vous de juger — hand the reader the material, neutrally, and let them
    judge the TOPIC (never the article's quality or the author)."""
    enjeux: list[str]        # 1–3 — Les enjeux de fond (the deeper problematics)
    objections: list[str]    # 1–3 — Les objections les plus solides (steel-man counters)
    angles_morts: list[str]  # 2–3 — Angles morts (topic omissions; no article-genre verdict)
    nuances: list[str]       # 2–3 — Nuances (topic-level qualifications; never the article's genre)


class NewsletterPresentation(BaseModel):
    """Reader-facing newsletter prose — the long-form prose render of the optimized
    carousel deck (same sections, same order, expanded; no grade/verdict)."""
    subject: str            # ≤12 words — email subject line
    preheader: str          # ≤15 words — inbox preview
    # Avant de vous lancer — L'essentiel is the title-less lead paragraph (opens the newsletter)
    essentiel: str            # L'essentiel de l'article — neutral ≈120–160-word restatement covering every element the analysis dissects (no critique)
    # Pourquoi cet article
    selection_headline: str   # the subtitle — punchy one-line "why we chose it"
    why_selected: str         # why we picked it to decrypt, expanded (2–3 sentences)
    payoff: str               # what the reader gains (2–3 sentences)
    # Avant de vous lancer (cont.)
    context: str              # Le contexte — the backdrop (3–4 sentences)
    reading_posture: str      # Comment le lire — 1 sentence on the article's rhetorical shape (primes; the toolkit lives in reflexes_critiques)
    # Au fil de la lecture
    decryptage: list[DecryptageItem]    # 5–7, article-ordered — neutral reading moments
    # Après la lecture — L'architecture de l'argument
    architecture: Architecture
    # Après la lecture — À emporter
    a_emporter: AEmporter
    # Après la lecture — À vous de juger
    verdict: AVousDeJuger
    # Après la lecture — Prolonger la réflexion
    cui_bono: str             # « À qui profite ce cadrage ? » — beneficiary + why the framing serves them
    go_further: list[Resource]  # 4–6 — Pour aller plus loin
    # (Avant de partir is static CTA chrome in the template — not generated)
    # signoff
    signoff: str              # closing line — "On sélectionne. On éclaire. Vous jugez."


class NewsletterDocument(BaseModel):
    """Output of the newsletter extractor: the full analysis + the newsletter prose."""
    analysis: "ArticleFullAnalysis"  # noqa: F821  (resolved at runtime)
    presentation: NewsletterPresentation


